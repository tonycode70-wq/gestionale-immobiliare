// ============ PAYMENTS (current year) ============
function generatePayments(): Payment[] {
  const payments: Payment[] = [];
  
  const leasePaymentConfigs = [
    { leaseId: 'lease-001', canone: 950, spese: 120 },
    { leaseId: 'lease-002', canone: 700, spese: 80 },
    { leaseId: 'lease-003', canone: 2500, spese: 250 },
    { leaseId: 'lease-004', canone: 1100, spese: 150 },
  ];

  let paymentId = 1;

  leasePaymentConfigs.forEach(config => {
    for (let month = 1; month <= 12; month++) {
      const isPast = month < currentMonth;
      const isCurrent = month === currentMonth;
      const importoTotale = config.canone + config.spese;
      
      let stato: Payment['stato_pagamento'] = 'ATTESO';
      let dataPagamento: string | undefined;
      let importoCanonePagato = 0;
      let importoSpesePagato = 0;

      if (isPast) {
        // Past months: mostly paid, some partial
        if (config.leaseId === 'lease-002' && month === currentMonth - 1) {
          // One partial payment
          stato = 'PARZIALE';
          importoCanonePagato = 500;
          importoSpesePagato = 80;
          dataPagamento = `${currentYear}-${String(month).padStart(2, '0')}-05`;
        } else {
          stato = 'PAGATO';
          importoCanonePagato = config.canone;
          importoSpesePagato = config.spese;
          dataPagamento = `${currentYear}-${String(month).padStart(2, '0')}-05`;
        }
      } else if (isCurrent) {
        // Current month: some paid, some waiting
        if (config.leaseId === 'lease-001' || config.leaseId === 'lease-003') {
          stato = 'PAGATO';
          importoCanonePagato = config.canone;
          importoSpesePagato = config.spese;
          dataPagamento = `${currentYear}-${String(month).padStart(2, '0')}-03`;
        } else {
          stato = 'ATTESO';
        }
      }

      payments.push({
        id: `payment-${String(paymentId++).padStart(3, '0')}`,
        lease_id: config.leaseId,
        competenza_anno: currentYear,
        competenza_mese: month,
        importo_canone_previsto: config.canone,
        importo_spese_previste: config.spese,
        importo_totale_previsto: importoTotale,
        data_scadenza: `${currentYear}-${String(month).padStart(2, '0')}-05`,
        data_pagamento: dataPagamento,
        importo_canone_pagato: importoCanonePagato,
        importo_spese_pagato: importoSpesePagato,
        importo_residuo_calcolato: importoTotale - importoCanonePagato - importoSpesePagato,
        stato_pagamento: stato,
        metodo_pagamento_effettivo: dataPagamento ? 'bonifico' : undefined,
        created_at: `${currentYear}-01-01T10:00:00Z`,
        updated_at: `${currentYear}-${String(month).padStart(2, '0')}-01T10:00:00Z`,
      });
    }
  });

  return payments;
}

export const mockPayments: Payment[] = generatePayments();

